def hex_a_texto(hex_string):
    """
    Convierte hexadecimal a texto.
    
    Parámetros:
        hex_string (str)
    
    Retorna:
        str
    """
    bytes_data = bytes.fromhex(hex_string)
    return bytes_data.decode('utf-8')


